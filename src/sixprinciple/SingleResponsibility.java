public class SingleResponsibility {
    /** 单一职责原则（SRP：Single responsibility principle）又称单一功能原则，面向对象五个基本原则（SOLID）之一。
    *   它规定一个类应该只有一个发生变化的原因。该原则由罗伯特·C·马丁（Robert C. Martin）于《敏捷软件开发：原则、模式和实践》一书中给出的。
    *   马丁表示此原则是基于汤姆·狄马克(Tom DeMarco)和Meilir Page-Jones的著作中的内聚性原则发展出的。
    *   所谓职责是指：类变化的原因。如果一个类有多于一个的动机被改变，那么这个类就具有多于一个的职责。
    *   而单一职责原则就是指一个类或者模块应该有且只有一个改变的原因。*/

}
